/*
What is Queue?
-> Queue is a Data Structure that follows FIFO techniques, which means data is inserted at the rear (end) and removed from the front.

Queue is a (Abstract Data Stucture), (Higher Level Data Stucture).

Not like (Array and Linked List is lower level Data Structure).

FIFO -> Queue follow the First In, First Out (FIFO) principle.

Queue Operation: enqueue(), dequeue(), front(), back(), size(), empty().

Using array the queue data structure takes O(N) complexity for dequeue() operation, so should have avoid array for queue.

Using linked list queue data structure takes O(1) complexity for all operations.
*/